class ExcelExporter:
    def __init__(self) -> None:
        self.font_name = "Calibri"
        self.font_title = Font(name=self.font_name, size=14, bold=True, color="1A1A2E")
        self.font_header = Font(name=self.font_name, size=11, bold=True, color="FFFFFF")
        self.font_data_bold = Font(name=self.font_name, size=11, bold=True)
        self.font_data = Font(name=self.font_name, size=11)
        self.font_italic = Font(
            name=self.font_name, size=9, italic=True, color="555555"
        )

        self.fill_header = PatternFill(
            start_color="1A1A2E", end_color="1A1A2E", fill_type="solid"
        )
        self.fill_alt = PatternFill(
            start_color="F5F5F5", end_color="F5F5F5", fill_type="solid"
        )
        self.fill_white = PatternFill(
            start_color="FFFFFF", end_color="FFFFFF", fill_type="solid"
        )

        self.fill_green_light = PatternFill(
            start_color="D4EDDA", end_color="D4EDDA", fill_type="solid"
        )
        self.fill_orange_light = PatternFill(
            start_color="FFF3CD", end_color="FFF3CD", fill_type="solid"
        )
        self.fill_red_light = PatternFill(
            start_color="F8D7DA", end_color="F8D7DA", fill_type="solid"
        )
        self.fill_yellow_light = PatternFill(
            start_color="FFFFE0", end_color="FFFFE0", fill_type="solid"
        )

        self.thin_side = Side(border_style="thin", color="CCCCCC")
        self.border_all = Border(
            left=self.thin_side,
            right=self.thin_side,
            top=self.thin_side,
            bottom=self.thin_side,
        )

    def _style_cell(
        self,
        cell: typing.Any,
        font: Font | None = None,
        fill: PatternFill | None = None,
        border: Border | None = None,
        alignment: Alignment | None = None,
    ) -> None:
        if font:
            cell.font = font
        if fill:
            cell.fill = fill
        if border:
            cell.border = border
        if alignment:
            cell.alignment = alignment

    def _style_range(
        self,
        ws: typing.Any,
        cell_range: str,
        font: Font | None = None,
        fill: PatternFill | None = None,
        border: Border | None = None,
        alignment: Alignment | None = None,
    ) -> None:
        for row in ws[cell_range]:
            for cell in row:
                self._style_cell(cell, font, fill, border, alignment)

    def _auto_fit_columns(self, ws: typing.Any) -> None:
        ws.freeze_panes = "A2"
        for col in ws.columns:
            max_len = 0
            col_letter = get_column_letter(col[0].column)
            for cell in col:
                # Check if cell is in a merged range
                is_merged = False
                for merged in ws.merged_cells.ranges:
                    if cell.coordinate in merged:
                        is_merged = True
                        break
                if is_merged:
                    continue
                val_str = str(cell.value if cell.value is not None else "")
                if len(val_str) > max_len:
                    max_len = len(val_str)
            ws.column_dimensions[col_letter].width = max(max_len + 3, 12)

    def _val(self, v: typing.Any) -> any:
        if v is None:
            return "n/a"
        try:
            if isinstance(v, (int, float)):
                return v
            return float(v)
        except (ValueError, TypeError):
            return str(v)

    import typing

    def export(self, result: ScanResult, path: str) -> None:
        wb = Workbook()
        ws_summary = wb.active
        ws_summary.title = "Summary"

        ws_valuation = wb.create_sheet(title="Valuation")
        ws_health = wb.create_sheet(title="Health")
        ws_growth = wb.create_sheet(title="Growth")
        ws_flags = wb.create_sheet(title="Red Flags")

        # ---------------- SUMMARY SHEET ----------------
        ws_summary.views.sheetView[0].showGridLines = True

        # A1 Branding
        ws_summary.merge_cells("A1:B1")
        ws_summary["A1"] = "Generated by telmus v0.1.0"
        self._style_range(
            ws_summary,
            "A1:B1",
            font=self.font_header,
            fill=self.fill_header,
            border=self.border_all,
            alignment=Alignment(horizontal="left", vertical="center"),
        )
        ws_summary.row_dimensions[1].height = 24

        summary_rows = [
            ("Company Name", result.company),
            ("Ticker", result.ticker),
            ("Exchange", result.exchange),
            ("Scan Date", datetime.date.today().isoformat()),
            ("Scan Duration", f"{result.scan_duration_ms} ms"),
            ("Highest Concern", result.highest_concern.lower()),
        ]

        for idx, (metric, val) in enumerate(summary_rows, start=2):
            fill = self.fill_alt if idx % 2 == 1 else self.fill_white
            ws_summary.cell(row=idx, column=1, value=metric)
            ws_summary.cell(row=idx, column=2, value=val)

            self._style_cell(
                ws_summary.cell(row=idx, column=1),
                font=self.font_data_bold,
                fill=fill,
                border=self.border_all,
                alignment=Alignment(horizontal="left"),
            )

            # Special fill for highest concern value cell
            if metric == "Highest Concern":
                c_val = str(val).lower()
                if "high" in c_val:
                    val_fill = self.fill_red_light
                elif "medium" in c_val:
                    val_fill = self.fill_orange_light
                else:
                    val_fill = self.fill_green_light
                self._style_cell(
                    ws_summary.cell(row=idx, column=2),
                    font=self.font_data,
                    fill=val_fill,
                    border=self.border_all,
                    alignment=Alignment(horizontal="left"),
                )
            else:
                self._style_cell(
                    ws_summary.cell(row=idx, column=2),
                    font=self.font_data,
                    fill=fill,
                    border=self.border_all,
                    alignment=Alignment(horizontal="left"),
                )

        # Analyst brief: merged in row 8-12
        ws_summary.cell(row=8, column=1, value="Analyst Brief")
        self._style_cell(
            ws_summary.cell(row=8, column=1),
            font=self.font_data_bold,
            fill=self.fill_white,
            border=self.border_all,
            alignment=Alignment(horizontal="left", vertical="top"),
        )

        ws_summary.merge_cells("B8:D12")
        ws_summary["B8"] = result.analyst_brief
        self._style_range(
            ws_summary,
            "B8:D12",
            font=self.font_data,
            fill=self.fill_white,
            border=self.border_all,
            alignment=Alignment(wrap_text=True, vertical="top"),
        )

        for r in range(8, 13):
            for c in range(2, 5):
                ws_summary.cell(row=r, column=c).border = self.border_all
                ws_summary.cell(row=r, column=c).fill = self.fill_white

        self._auto_fit_columns(ws_summary)

        # ---------------- VALUATION SHEET ----------------
        ws_valuation.views.sheetView[0].showGridLines = True
        ws_valuation.append(["Metric", "Value", "Status"])
        self._style_range(
            ws_valuation,
            "A1:C1",
            font=self.font_header,
            fill=self.fill_header,
            border=self.border_all,
            alignment=Alignment(horizontal="left"),
        )

        pe_status = result.valuation.flag if result.valuation.flag else "ok"
        pb_status = result.valuation.flag if result.valuation.flag else "ok"
        ev_status = result.valuation.flag if result.valuation.flag else "ok"
        vs_status = "flagged" if result.valuation.vs_sector == "expensive" else "ok"

        val_data = [
            ("P/E Ratio", result.valuation.pe_ratio, pe_status),
            ("P/B Ratio", result.valuation.pb_ratio, pb_status),
            ("EV/EBITDA", result.valuation.ev_ebitda, ev_status),
            ("Sector Comparison", result.valuation.vs_sector, vs_status),
        ]

        for idx, (m, v, s) in enumerate(val_data, start=2):
            fill = self.fill_alt if idx % 2 == 1 else self.fill_white
            ws_valuation.cell(row=idx, column=1, value=m)
            ws_valuation.cell(row=idx, column=2, value=self._val(v))
            ws_valuation.cell(row=idx, column=3, value=s)

            self._style_cell(
                ws_valuation.cell(row=idx, column=1),
                font=self.font_data_bold,
                fill=fill,
                border=self.border_all,
                alignment=Alignment(horizontal="left"),
            )
            self._style_cell(
                ws_valuation.cell(row=idx, column=2),
                font=self.font_data,
                fill=fill,
                border=self.border_all,
                alignment=Alignment(horizontal="left"),
            )

