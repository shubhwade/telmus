            ws.cell(row=idx, column=7, value=self._val(r.health.altman_z))
            ws.cell(row=idx, column=8, value=self._val(r.growth.revenue_cagr_3y))
            ws.cell(row=idx, column=9, value=r.growth.margin_trend or "n/a")
